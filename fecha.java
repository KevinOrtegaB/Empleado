

public class fecha
{
    private Integer dia,mes,año;
    public fecha(Integer dia,Integer mes,Integer año)
    {
      this.dia=dia;
      this.mes=mes;
      this.año=año;
    }
    public Integer getDia(){
      return dia;
    }
     public Integer getMes(){
      return dia;
    }
     public Integer getAño(){
      return dia;
    }
}
